--- 从指定难度的题库中随机抽取 n 道不重复的题目
---@param level string 考试等级 id（tongshi/xiangshi/huishi/dianshi）
---@param n number 需要的题目数量
---@return table[] 题目列表
function ExamQuestions.Pick(level, n)
    local pool = ExamQuestions[level]
    if not pool or #pool == 0 then return {} end

    -- 复制索引数组并洗牌
    local indices = {}
    for i = 1, #pool do indices[i] = i end

    -- Fisher-Yates 洗牌
    for i = #indices, 2, -1 do
        local j = math.random(1, i)
        indices[i], indices[j] = indices[j], indices[i]
    end

    -- 取前 n 个
    local result = {}
    local count = math.min(n, #indices)
    for i = 1, count do
        result[i] = pool[indices[i]]
    end
    return result
end

--- 获取指定等级的考试配置
---@param levelId string
---@return table|nil { total, pass, name }
function ExamQuestions.GetConfig(levelId)
    return ExamQuestions.EXAM_CONFIG[levelId]
end

